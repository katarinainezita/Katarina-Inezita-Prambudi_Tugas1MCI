const kalkulator = {
    tambah: function(num1, num2) {
      return num1 + num2;
    },
    kurang: function(num1, num2) {
      return num1 - num2;
    },
    kali: function(num1, num2) {
      return num1 * num2;
    },
    bagi: function(num1, num2) {
      return num1 / num2;
    }
  };
  
  module.exports = kalkulator;